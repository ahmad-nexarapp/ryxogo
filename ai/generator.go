// UpdateSchema re-scans the project and writes the latest schema to .ryxogo/schema.json
func UpdateSchema(root string) error {
	// This is called by rxgo mcp serve on startup and file changes
	schemaPath := filepath.Join(root, ".ryxogo", "schema.json")

	lines := strings.Builder{}
	lines.WriteString(fmt.Sprintf(`{
  "_note": "Auto-generated by rxgo mcp serve. Updated: %s",
  "framework": "ryxogo"
}`, time.Now().Format(time.RFC3339)))

	return os.WriteFile(schemaPath, []byte(lines.String()), 0644)
}
