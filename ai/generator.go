// Package ai generates AI tool configuration files for RyxoGo projects.
// Run automatically by `rxgo new` and `rxgo init`.
// Keeps Cursor, Claude Code, Codex, and Copilot perfectly aligned.
package ai

import (
	"fmt"
	"os"
	"path/filepath"
	"strings"
	"time"
)

// Generator creates AI tool config files for a RyxoGo project
type Generator struct {
	root    string
	appName string
}

// NewGenerator creates a generator for the project at root
func NewGenerator(root, appName string) *Generator {
	return &Generator{root: root, appName: appName}
}

// GenerateAll creates all AI config files
func (g *Generator) GenerateAll() error {
	files := map[string]func() (string, error){
		".cursorrules":          g.cursorRules,
		"CLAUDE.md":             g.claudeMD,
		"AGENTS.md":             g.agentsMD,
		".github/copilot.md":    g.copilotMD,
		".ryxogo/context.md":    g.ryxogoContext,
		".ryxogo/mcp.json":      g.mcpConfig,
		".ryxogo/schema.json":   g.schemaPlaceholder,
	}

	for path, fn := range files {
		content, err := fn()
		if err != nil {
			return fmt.Errorf("generating %s: %w", path, err)
		}

		fullPath := filepath.Join(g.root, path)
		if err := os.MkdirAll(filepath.Dir(fullPath), 0755); err != nil {
			return err
		}
		if err := os.WriteFile(fullPath, []byte(content), 0644); err != nil {
			return err
		}
	}

	return nil
}

